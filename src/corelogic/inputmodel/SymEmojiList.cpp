struct SymbolEmojiList : public ISymbolEmojiList
{
	std::shared_ptr<IPhraseList> GetList(SymbolEmojiCategory category, uint16_t classId)
	{
		switch (category)
		{
		case SymbolEmojiCategory::Human:
			return GetListChar32(s_EmojiHuman, classId);
		case SymbolEmojiCategory::Animal:
			return GetListChar32(s_EmojiAnimal, classId);
		case SymbolEmojiCategory::Food:
			return GetListChar32(s_EmojiFood, classId);
		case SymbolEmojiCategory::TimeRegion:
			return GetListChar32(s_EmojiTimeRegion, classId);
		case SymbolEmojiCategory::Game:
			return GetListChar32(s_EmojiGame, classId);
		case SymbolEmojiCategory::Items:
			return GetListChar32(s_EmojiItems, classId);
		case SymbolEmojiCategory::SymbolMarks:
			return GetListChar32(s_EmojiSymbolMarks, classId);
		case SymbolEmojiCategory::Flags:
			return GetListChar32(s_EmojiFlags, classId);

		case SymbolEmojiCategory::SymStandard:
			return GetListChar16(s_SymbolNormal, classId);
		case SymbolEmojiCategory::SymParenthes:
			return GetListChar16(s_SymbolParenthes, classId);
		case SymbolEmojiCategory::SymArrows:
			return GetListChar16(s_SymbolArrows, classId);
		case SymbolEmojiCategory::SymMath:
			return GetListChar16(s_SymbolMath, classId);
		case SymbolEmojiCategory::Numbers:
			return GetListChar16(s_SymbolNumber, classId);
		case SymbolEmojiCategory::Images:
			return GetListChar16(s_SymbolImages, classId);
		case SymbolEmojiCategory::FullWidth:
			return GetListChar16(s_SymbolFullWidth, classId);
		default:
		case SymbolEmojiCategory::Max:
			THROW_IF_FALSE_MSG(false, "Unknown category");
		}
	}

	std::shared_ptr<IPhraseList> GetListChar32(const char32_t* stream, uint16_t classId)
	{
		auto phraseList = FACTORYCREATE(PhraseList);

		while (*stream != 0) {
			char16_t utf16buf[128];
			char16_t* endPtr;
			size_t remainedSize;

			std::tie(endPtr, remainedSize) = convert_utf32_to_utf16_text(utf16buf, COUNT_OF(utf16buf), stream);
			auto emojiText = RefString(utf16buf);

			auto primitive = FACTORYCREATE(Primitive);
			primitive->Display(emojiText);
			primitive->Reading(emojiText);
			primitive->Class(classId);

			auto phrase = FACTORYCREATE(Phrase);
			phrase->Push(primitive);

			phraseList->Push(phrase);

			for (; *stream != 0; ++stream) ;
			++stream;
		}

		return phraseList;
	}

	std::shared_ptr<IPhraseList> GetListChar16(const char16_t* stream, uint16_t classId)
	{
		auto phraseList = FACTORYCREATE(PhraseList);

		while (*stream != 0) {
			auto emojiText = RefString(stream);

			auto primitive = FACTORYCREATE(Primitive);
			primitive->Display(emojiText);
			primitive->Reading(emojiText);
			primitive->Class(classId);

			auto phrase = FACTORYCREATE(Phrase);
			phrase->Push(primitive);

			phraseList->Push(phrase);

			for (; *stream != 0; ++stream) ;
			++stream;
		}

		return phraseList;
	}

};

